function tanggal($tgl)
{
    $tanggal = substr($tgl, 8, 2);
    $bulan = getBulan(substr($tgl, 5, 2));
    $tahun = substr($tgl, 0, 4);
    return $tanggal . ' ' . $bulan . ' ' . $tahun;
}

function getBulan($bln)
{
    switch ($bln) {
        case 1:
            return "Januari";
        case 2:
            return "Februari";
        case 3:
            return "Maret";
        case 4:
            return "April";
        case 5:
            return "Mei";
        case 6:
            return "Juni";
        case 7:
            return "Juli";
        case 8:
            return "Agustus";
        case 9:
            return "September";
        case 10:
            return "Oktober";
        case 11:
            return "November";
        case 12:
            return "Desember";
        default:
            return "Tidak Diketahui"; // Menambahkan default jika bulan tidak valid
    }
}

function hari($hari)
{
    $hari = date("D", strtotime($hari));
    switch ($hari) {
        case 'Sun':
            return "Minggu";
        case 'Mon':
            return "Senin";
        case 'Tue':
            return "Selasa";
        case 'Wed':
            return "Rabu";
        case 'Thu':
            return "Kamis";
        case 'Fri':
            return "Jumat";
        case 'Sat':
            return "Sabtu";
        default:
            return "Tidak Diketahui";
    }
}
